 56   CONTINUE
      WRITE(IOUT,*) '(IFLLOC(I,N),I=1,4),(FLWRAT(L,N),L=1,NBDTIM):'
      WRITE(IOUT,*) (IFLLOC(I,N),I=1,4)
      WRITE(IOUT,*) (FLWRAT(L,N),L=1,NBDTIM)
