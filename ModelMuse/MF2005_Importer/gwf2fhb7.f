C5B------SPECIFIED-FLOW CELL LOCATIONS
!!      WRITE(IOUT,*) 
!!     &    '(IFLLOC(I,N),I=1,4),(FLWRAT(NS+L,N),L=1,NBDTIM):'
!!      WRITE(IOUT,*) 
!!     &    (IFLLOC(I,N),I=1,4),(FLWRAT(NS+L,N),L=1,NBDTIM)
!      IF(IFHBPT.GT.0) THEN
!         WRITE(IOUT,58) (IFLLOC(I,N),I=1,4),
