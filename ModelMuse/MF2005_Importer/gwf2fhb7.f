!         WRITE(IOUT,52)
! 52      FORMAT(1X,'LAYER  ROW  COL IAUX              FLOW RATES')
!         ND=MIN0(79,19+NBDTIM*12)
!         WRITE(IOUT,54) (DSH1,M=1,ND)
! 54      FORMAT(1X,78A1)
