C6------READ CELL INDICIES AND SPECIFIED-HEAD VALUES
 70   IF(NHED.LT.1) GO TO 300
      READ(IN,*) IFHBUN,CNSTM,IFHBPT
      WRITE(IOUT,*) 'IFHBUN,IFHBPT3:'
      WRITE(IOUT,*) IFHBUN,IFHBPT
